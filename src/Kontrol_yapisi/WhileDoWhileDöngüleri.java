package Kontrol_yapisi;

public class WhileDoWhileDöngüleri {
    public static void main(String[] args) {
        int i = 5;
        while (i<20) {
            System.out.println(i);
            i++;
        }
        //kod bir kere çalıştırılır daha sonra şarta bakılır
int s1=4;
        do {
            System.out.println(s1);
            s1++;

        }while (s1<20);
    }
}
